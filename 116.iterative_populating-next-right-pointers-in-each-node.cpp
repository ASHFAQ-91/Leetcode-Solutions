class Solution {
public:
    Node* connect(Node* root) {
        if(!root){
            return NULL;
        }
        queue<Node*> q;
        q.push(root);
        while(q.empty() == false){
            int size = q.size();
            while(size-- > 0){
                Node* curr = q.front();
                q.pop();
                if(size > 0){
                    curr->next = q.front();
                }
                if(curr->left){
                    q.push(curr->left);
                }
                if(curr->right){
                    q.push(curr->right);
                }
            }
        }
        return root;
    }
};
